#include "3-calc.h"

/**
 * op_add - adds two int
 * @a: int
 * @b: int
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int
 * @a: int
 * @b: int
 *
 * Return: the sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 * @a: int
 * @b: int
 *
 * Return: the product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 * @a: int
 * @b: int
 *
 * Return: the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find the modulus
 * @a: int
 * @b: int
 *
 * Return: the remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
